/* ==========================================================================
 * 路径工具 (PathUtils)
 * ==========================================================================
 * 提供基于 CONFIG.basePath 的路径解析方法，确保所有运行时路径
 * 在不同部署环境下（根目录 / GitHub Pages 子目录 / 自定义子目录）正确生效。
 *
 * 【设计原则】
 * - HTML 中的 CSS/JS 引入使用相对路径（./ ../），天然适配任何部署位置
 * - JS 运行时路径（数据加载、页面跳转、媒体资源）通过 PathUtils 解析
 * - JSON 数据中的资源路径（如 /artwork/audio/S001.mp3）也通过 PathUtils 解析
 * - 修改部署位置只需改 CONFIG.basePath，一处配置全局生效
 * ========================================================================== */

const PathUtils = {

    /**
     * 将站点根相对路径解析为包含 basePath 的完整路径
     *
     * 规则：
     * - 以 '/' 开头的路径视为"站点根相对路径"，自动拼合 basePath
     * - 不以 '/' 开头的路径（如 URL、相对路径、data: URI）原样返回
     * - null / undefined / 空字符串原样返回
     *
     * @param {string|null|undefined} path - 待解析的路径
     * @returns {string} 解析后的完整路径
     *
     * @example
     * // basePath = '/my-repo/'
     * PathUtils.resolve('/data/artwork.json')  // → '/my-repo/data/artwork.json'
     * PathUtils.resolve('/page/works.html')    // → '/my-repo/page/works.html'
     * PathUtils.resolve('https://example.com') // → 'https://example.com'（外部URL不变）
     * PathUtils.resolve(null)                  // → null
     */
    resolve(path) {
        /* 空值直接返回 */
        if (path == null || path === '') return path;

        /* 只对以 / 开头的站点根相对路径拼合 basePath */
        if (path.startsWith('/')) {
            return CONFIG.basePath + path.slice(1);
        }

        /* 其他路径（相对路径、外部 URL、data: URI 等）原样返回 */
        return path;
    },

    /**
     * 跳转到指定页面（站点根相对路径）
     *
     * 自动拼合 basePath 后执行跳转，是全站统一的页面导航方法。
     *
     * @param {string} path - 目标页面路径（站点根相对，如 '/page/works.html'）
     *
     * @example
     * PathUtils.navigate('/page/works.html')
     * PathUtils.navigate('/page/work-detail.html?id=S001')
     */
    navigate(path) {
        window.location.href = PathUtils.resolve(path);
    },

    /**
     * 获取当前页面的站点根相对路径（去除 basePath 前缀）
     *
     * 用于判断当前处于哪个页面，不因部署位置变化而失效。
     *
     * @returns {string} 站点根相对路径，如 '/page/works.html'
     */
    getCurrentPath() {
        const fullPath = window.location.pathname;
        /* 去除 basePath 前缀，还原为站点根相对路径 */
        if (CONFIG.basePath !== '/' && fullPath.startsWith(CONFIG.basePath)) {
            return '/' + fullPath.slice(CONFIG.basePath.length);
        }
        return fullPath;
    },
};
